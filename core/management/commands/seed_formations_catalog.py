class Command(BaseCommand):
    help = (
        "Seed all specialties from the 2019 Algerian vocational catalogue "
        f"({len(SPECIALTIES)} categories across 23 branches)."
    )

    def add_arguments(self, parser):
        parser.add_argument(
            "--force",
            action="store_true",
            help="Overwrite name for existing specialties (code is the lookup key).",
        )

    def handle(self, *args, **options):
        force = options["force"]

        try:
            from formations.models import FormationCategory
        except ImportError as exc:
            self.stderr.write(self.style.ERROR(f"Import error: {exc}"))
            return

        created = updated = skipped = 0

        for spec_code, name, _branch_code in SPECIALTIES:
            obj, was_created = FormationCategory.objects.get_or_create(
                code=spec_code,
                defaults={"name": name},
            )

            if was_created:
                created += 1
            elif force:
                obj.name = name
                obj.save(update_fields=["name", "updated_at"])
                updated += 1
            else:
                skipped += 1

        self.stdout.write(
            self.style.SUCCESS(
                f"✓ FormationCategory specialties — "
                f"{created} created, {updated} updated, {skipped} skipped "
                f"(total {len(SPECIALTIES)})."
            )
        )
        if skipped and not force:
            self.stdout.write(
                "  Tip: use --force to overwrite names for existing entries."
            )
